- (BOOL)isIpAddress {
    NSArray *strArray = [self componentsSeparatedByString:@"."];
    NSInteger count = [strArray count];
    if (count != 4) {
        return NO;
    }
    for (NSInteger index = 0; index < count; index++) {
        NSString *ipPart = [strArray objectAtIndex:index];
        if ([ipPart length] > 3) {
            return NO;
        }
        for (NSInteger k = 0; k < [ipPart length]; k++) {
            unichar ch = [ipPart characterAtIndex:k];
            if (!(ch >= '0' && ch <= '9')) {
                return NO;
            }
        }
    }
    return YES;
}

