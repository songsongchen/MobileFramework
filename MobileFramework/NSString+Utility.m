// 字符串首字母（包括汉字）
+ (NSString *)firstLetterWithTitle:(NSString *)title {
    int cLetter = 0;
    if (title == nil || 0 == [title length] || [title hasPrefix:@"匿名"]) {
        cLetter = '#';
    }
    else {
        unichar firstLetter = [title characterAtIndex:0];
        if ((firstLetter > 64 && firstLetter < 91) || (firstLetter > 96 && firstLetter < 123)) {
            cLetter = firstLetter;
        }
        else {
            cLetter = pinyinFirstLetter((unsigned short) firstLetter);
        }
        if (cLetter > 95) {
            cLetter -= 32;
        }
    }
    return [NSString stringWithFormat:@"%c", cLetter];
}

// 字符串拼音首字母（包括汉字）
+ (NSString *)firstLettersWithTitle:(NSString *)title {
    if (title == nil || 0 == [title length] || [title hasPrefix:@"匿名"]) {
        return nil;
    }
    NSMutableString *string = [[NSMutableString alloc] init];
    NSInteger count = [title length];
    unichar cLetter = 0;
    unichar firstLetter = 0;
    for (NSInteger i = 0; i < count; i++) {
        firstLetter = [title characterAtIndex:i];
        if ((firstLetter > 64 && firstLetter < 91) || (firstLetter > 96 && firstLetter < 123)) {
            cLetter = firstLetter;
        }
        else {
            cLetter = pinyinFirstLetter((unsigned short) firstLetter);
            if (cLetter == '#' && firstLetter < 128) {
                cLetter = firstLetter;
            }
        }
        if (cLetter > 95) {
            cLetter -= 32;
        }
        [string appendFormat:@"%c", cLetter];
    }
    return string;
}

// MD5加密
- (NSString *)md5FromString {
    const char *cStr = [self UTF8String];
    unsigned char result[CC_MD5_DIGEST_LENGTH];
    CC_MD5(cStr, strlen(cStr), result);
    NSMutableString *hash = [NSMutableString string];
    for (int i = 0; i < CC_MD5_DIGEST_LENGTH; i++) {
        [hash appendFormat:@"%02X", result[i]];
    }
    return [hash lowercaseString];
}


